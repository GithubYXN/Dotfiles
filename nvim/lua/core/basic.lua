-- set line number
vim.opt.number = true

-- set relative number
vim.opt.relativenumber = true

-- set current line highlight
vim.opt.cursorline = true

-- set column width
vim.opt.colorcolumn = "80"

-- set tab to 4 spaces
vim.opt.expandtab = true
vim.opt.tabstop = 4
vim.opt.shiftwidth = 0

-- auto reload while changed outside
vim.opt.autoread = true

-- set leader
vim.g.mapleader = " "

-- disable mode display
vim.opt.showmode = false

-- enable clipboard share
vim.opt.clipboard = "unnamedplus"

-- case insensitive if there is no uppercase in content
vim.opt.ignorecase = true
vim.opt.smartcase = true

-- cancle matched result highlight after search
vim.opt.hlsearch = false

